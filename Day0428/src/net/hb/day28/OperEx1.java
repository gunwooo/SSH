package net.hb.day28;

public class OperEx1 {
	public static void main(String[] args) {// Page 74
		System.out.println("OperEx1.java ");
		int a, b, c;
		a = 20;
		b = 7;

		c = a + b;
		System.out.println(a + "+" + b + "=" + c);
		c = a - b;
		System.out.println(a + "-" + b + "=" + c);
		c = a % b;
		System.out.println(a + "%" + b + "=" + c);
	}// main end
}// class end
